#include "ChessPieces.hpp"

////////////////////////////////////////////////////////////////////////////////////
////////////////////////PUBLIC FÖLL/////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////
ChessPieces::ChessPieces(){
    _icon = 'E';
    _xPlacement = -1;
    _yPlacement = -1;
}

void ChessPieces::initPiece( char icon, int xPlace, int yPlace){
    _icon = icon;
    _xPlacement = xPlace;
    _yPlacement = yPlace;
}

char ChessPieces::GetIcon(){
    return _icon;
}
int ChessPieces::GetXPos(){
    return _xPlacement;
}
int ChessPieces::GetYPos(){
    return _yPlacement;
}

